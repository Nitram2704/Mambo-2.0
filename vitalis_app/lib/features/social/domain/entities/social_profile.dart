class SocialProfile {
  final String name;
  final String initials;
  final String username;
  final int level;
  final int streak;
  final int logros;
  final int grupos;

  const SocialProfile({
    required this.name,
    required this.initials,
    required this.username,
    required this.level,
    required this.streak,
    required this.logros,
    required this.grupos,
  });
}
